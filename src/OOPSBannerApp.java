
/**
 * OOPS Banner Application
 * UC3: Render OOPS as Banner using String.join()
 *
 * @author Sameer
 * @version 3.0
 */
public class OOPSBannerApp {
    public static void main(String[] args) {

        System.out.println(String.join("", "   ***   ", "   ***   ", "  ****** ", "  ****** "));
        System.out.println(String.join("", "  ** **  ", "  ** **  ", "  **    **", " **       "));
        System.out.println(String.join("", " **   ** ", " **   ** ", "  **    **", " **       "));
        System.out.println(String.join("", " **   ** ", " **   ** ", "  **    **", " **       "));
        System.out.println(String.join("", " **   ** ", " **   ** ", "  ****** ", "  ****** "));
        System.out.println(String.join("", " **   ** ", " **   ** ", "  **     ", "       **"));
        System.out.println(String.join("", " **   ** ", " **   ** ", "  **     ", "       **"));
        System.out.println(String.join("", "  ** **  ", "  ** **  ", "  **     ", " **    **"));
        System.out.println(String.join("", "   ***   ", "   ***   ", "  **     ", "  ****** "));
    }
}


